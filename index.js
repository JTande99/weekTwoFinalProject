function openMenu() {
    document.querySelector('.nav__menu--card').style.display = 'flex';
    document.querySelector('.nav__menu--button').style.display = 'none';
    document.querySelector('.nav__menu--close').style.display = 'initial';
}

function closeMenu() {
    document.querySelector('.nav__menu--card').style.display = 'none';
    document.querySelector('.nav__menu--button').style.display = 'block';
    document.querySelector('.nav__menu--close').style.display = 'none';
}

